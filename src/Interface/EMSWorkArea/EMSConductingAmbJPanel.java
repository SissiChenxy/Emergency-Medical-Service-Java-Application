    private void populateList() {
        DefaultListModel listModel = new DefaultListModel();
        EMSEnterprise eMSEnterprise = (EMSEnterprise) enterprise;
        for (Ambulance amb : eMSEnterprise.getAmbulances()) {
            if (amb.getStatus().equals("Free")) {
                listModel.addElement(amb);
            }
        }
        ambList.setModel(listModel);
    }

    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        model.setRowCount(0);
        for (Organization org : enterprise.getOrganizationDirectory().getOrganizationList()) {
            for (UserAccount account : org.getUserAccountDirectory().getUserAccountList()) {
                if (account.getRole() instanceof FirstAiderRole && account.getStatus().equals("Available")) {
                    Object row[] = new Object[1];
                    row[0] = account;
                    model.addRow(row);
                }
            }
        }
    }

