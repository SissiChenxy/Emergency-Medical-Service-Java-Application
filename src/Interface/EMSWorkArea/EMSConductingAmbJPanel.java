    private void assignBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_assignBtnActionPerformed
        // TODO add your handling code here:
        int[] selectedRow = table.getSelectedRows();
        if (selectedRow.length > 0) {
            Ambulance amb = (Ambulance) ambList.getSelectedValue();
            if (amb == null) {
                JOptionPane.showMessageDialog(null, "Please select an ambluance.");
            } else {
                amb.setStatus("On duty");
                AmbulanceService service = new AmbulanceService(amb);
                for (int eachRow : selectedRow) {
                    UserAccount aider = (UserAccount) table.getValueAt(eachRow, 0);
                    aider.setStatus("Unavailable");
                    service.getListOfAiders().add(aider);
                }
                request.setAmbulanceService(service);
                ambulanceTextField.setText(amb.getName());
                request.setStatus("EMS Ready");
            }
        }
    }//GEN-LAST:event_assignBtnActionPerformed

