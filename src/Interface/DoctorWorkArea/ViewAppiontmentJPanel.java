    private void jToggleButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton4ActionPerformed
        // TODO add your handling code here:
        String dianose = dianosetext.getText();
        Date now = new Date();
        
        workRequest.setResolveDate(now);
        workRequest.setReceiver(account);
        workRequest.setDianose(dianose);
        workRequest.setStatus("FINISH");
        workRequest.setPrescriptionList(prescriptionList);
        String patientUser = patient.getUsername();
        WorkQueue workQueue = hospitalEnterprise.getHistory().get(patientUser);
        if(workQueue!=null){
            workQueue.getWorkRequestList().add(workRequest);
        }else{
            workQueue = new WorkQueue();
            workQueue.getWorkRequestList().add(workRequest);
            hospitalEnterprise.getHistory().put(patientUser, workQueue);
        }
        doctorRole.getWorkQueue().remove(workRequest);
        
        userProcessContainer.remove(this);
        Component[] componentArray = userProcessContainer.getComponents();
        Component component = componentArray[componentArray.length - 1];
        AppointmentJPanel dwjp = (AppointmentJPanel) component;
        dwjp.populateTable(doctorRole.getTodayQueue());
        CardLayout layout = (CardLayout)userProcessContainer.getLayout();
        layout.show(userProcessContainer,"appointmentJPanel");
    }//GEN-LAST:event_jToggleButton4ActionPerformed

