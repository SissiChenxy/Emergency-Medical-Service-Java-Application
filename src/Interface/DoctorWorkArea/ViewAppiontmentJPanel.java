    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        // TODO add your handling code here:
        Medicine medicine = (Medicine)jComboBox1.getSelectedItem();
        if(medicine!=null){
            int num = (Integer)quantitySpinner.getValue();
            String attention = this.attention.getText();
            Prescription prescription = new Prescription();
            prescription.setAttention(attention);
            prescription.setMedicine(medicine);
            prescription.setNum(num);
            prescriptionList.getPrescriptions().add(prescription);
        }
        populatePre();
        
    }//GEN-LAST:event_jToggleButton1ActionPerformed

