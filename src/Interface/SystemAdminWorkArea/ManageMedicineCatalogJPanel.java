    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = vaccineTable.getSelectedRow();
        if(selectedRow >= 0)
        {
            Medicine medicine = (Medicine)vaccineTable.getValueAt(selectedRow, 0);
            system.getMedicineCatalog().deleteMedicine(medicine);
            JOptionPane.showMessageDialog(null,"Medicine has been deleted!");
            populateTable();
        }
        else
            JOptionPane.showMessageDialog(null,"Please select any row!");
    }//GEN-LAST:event_deleteButtonActionPerformed

    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        // TODO add your handling code here:
        DefaultTableModel dtm = (DefaultTableModel)vaccineTable.getModel();
        int row = dtm.getRowCount();
        int column = dtm.getColumnCount();
        for (int i = 0; i < row; i++) {
            for (int j = 0; j < column; j++) {
                switch (column) {
                    case 0:
                        system.getMedicineCatalog().getMedicineByIndex(i).setName(dtm.getValueAt(i, j).toString());
                    case 1:
                        system.getMedicineCatalog().getMedicineByIndex(i).setDescription(dtm.getValueAt(i, j).toString());  
                    case 2:
                        system.getMedicineCatalog().getMedicineByIndex(i).setDosageAndAdministration(dtm.getValueAt(i, j).toString());      
                }
            }
        }
        JOptionPane.showMessageDialog(null,"Vaccine Edited Successfully!");
    }//GEN-LAST:event_editButtonActionPerformed

