    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) vaccineTable.getModel();
        model.setRowCount(0);
        for (Medicine medicine : system.getMedicineCatalog().getMedicineList()) {
            Object[] row = new Object[3];
            
            row[0] = medicine;
            row[1] = medicine.getDescription();
            row[2] = medicine.getDosageAndAdministration();
            
            model.addRow(row);
        }
    }


